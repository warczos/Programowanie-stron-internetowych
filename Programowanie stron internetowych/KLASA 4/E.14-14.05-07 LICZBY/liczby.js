function zakres() {
    var x = parseInt(prompt("Podaj liczbę (START)"));
    var y = parseInt(prompt("Podaj liczbę (KONIEC)"));
    var z = " "
    for (i = x; i <= y; i++) {
        z += i + ", "
    }
    for (i = x; i >= y; i--) {
        z += i + ", "
    }
    alert(z);
}























































































